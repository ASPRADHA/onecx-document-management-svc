package org.tkit.document.management.domain.models.entities;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import org.tkit.quarkus.jpa.models.TraceableEntity;

import lombok.Getter;
import lombok.Setter;

/**
 * The SupportedMimeType entity.
 */
@Getter
@Setter
@Entity
@Table(name = "DM_SUPPORTED_MIME_TYPE")
public class SupportedMimeType extends TraceableEntity {
    /**
     * Name of the supported mime-type.
     */
    @Column(name = "NAME")
    private String name;
    /**
     * Description of the supported mime-type.
     */
    @Column(name = "DESCRIPTION")
    private String description;

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = super.hashCode();
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((description == null) ? 0 : description.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (!super.equals(obj))
            return false;
        if (getClass() != obj.getClass())
            return false;
        SupportedMimeType other = (SupportedMimeType) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (description == null) {
            if (other.description != null)
                return false;
        } else if (!description.equals(other.description))
            return false;
        return true;
    }
}
