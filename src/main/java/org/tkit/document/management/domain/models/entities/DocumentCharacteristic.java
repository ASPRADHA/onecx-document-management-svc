package org.tkit.document.management.domain.models.entities;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import org.tkit.quarkus.jpa.models.TraceableEntity;

import lombok.Getter;
import lombok.Setter;

/**
 * The DocumentCharacteristic entity.
 */
@Getter
@Setter
@Entity
@Table(name = "DM_DOCUMENT_CHARACTERISTIC")
public class DocumentCharacteristic extends TraceableEntity {
    /**
     * Name of the characteristic.
     */
    @Column(name = "NAME")
    private String name;
    /**
     * Value of the characteristic.
     */
    @Column(name = "VALUE")
    private String value;

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = super.hashCode();
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((value == null) ? 0 : value.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (!super.equals(obj))
            return false;
        if (getClass() != obj.getClass())
            return false;
        DocumentCharacteristic other = (DocumentCharacteristic) obj;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (value == null) {
            if (other.value != null)
                return false;
        } else if (!value.equals(other.value))
            return false;
        return true;
    }
}
